package com.example.Laborator_7.entity;

public class Apartinator {

    private int id_apartinator;

    private String nume;

    private String prenume;

    private String numarTelefon;

    public int getId_apartinator() {
        return id_apartinator;
    }

    public void setId_apartinator(int id_apartinator) {
        this.id_apartinator = id_apartinator;
    }

    public String getNume() {
        return nume;
    }

    public void setNume(String nume) {
        this.nume = nume;
    }

    public String getPrenume() {
        return prenume;
    }

    public void setPrenume(String prenume) {
        this.prenume = prenume;
    }

    public String getNumarTelefon() {
        return numarTelefon;
    }

    public void setNumarTelefon(String numarTelefon) {
        this.numarTelefon = numarTelefon;
    }
}